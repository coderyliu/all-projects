<template>
  <div>
    <el-breadcrumb separator=">">
      <el-breadcrumb-item :to="{ path: '/home' }">首页</el-breadcrumb-item>
      <el-breadcrumb-item>商品管理</el-breadcrumb-item>
      <el-breadcrumb-item>添加商品</el-breadcrumb-item>
    </el-breadcrumb>
    <el-card>
      <!-- 提示区 -->
      <el-alert title='添加商品信息' type='info' center show-icon :closable='false'></el-alert>
      <!-- 步骤条区 -->
      <el-steps :active="activeIndex-0" finish-status="success" align-center>
        <el-step title="基本信息"></el-step>
        <el-step title="商品参数"></el-step>
        <el-step title="商品属性"></el-step>
        <el-step title="商品图片"></el-step>
        <el-step title="商品内容"></el-step>
        <el-step title="完成"></el-step>
      </el-steps>
      <!-- tab栏 -->
      <el-form :model='addForm' ref='addFormRef' :rules='addFormRules' label-position='top' label-width='100px'>
        <el-tabs :tab-position="tabPosition" style="height: 600px;" v-model='activeIndex' :before-leave="beforeTabLeave" @tab-click="tabClicked">
          <el-tab-pane label="基本信息" name='0'>
            <el-form-item label="商品名称" prop="goods_name">
              <el-input v-model="addForm.goods_name"></el-input>
            </el-form-item>
            <el-form-item label="商品价格" prop="goods_price">
              <el-input v-model="addForm.goods_price" type="number"></el-input>
            </el-form-item>
            <el-form-item label="商品重量" prop="goods_weight">
              <el-input v-model="addForm.goods_weight" type="number"></el-input>
            </el-form-item>
            <el-form-item label="商品数量" prop="goods_number">
              <el-input v-model="addForm.goods_number" type="number"></el-input>
            </el-form-item>
            <!-- 级联选择器 -->
            <el-form-item label="商品分类" prop="goods_cat">
              <el-cascader v-model="addForm.goods_cat" :options="cateList"
                :props="{ expandTrigger: 'hover', ...cateProps }" @change="handleChange"></el-cascader>
            </el-form-item>
          </el-tab-pane>
          <el-tab-pane label="商品参数" name='1'>
            <!-- 渲染表单的item项 -->
            <el-form-item :label="item.attr_name" v-for="item in manyTableData" :key="item.attr_id">
              <el-checkbox-group v-model="item.attr_vals">
                <el-checkbox border :label="item" v-for="(item, i) in item.attr_vals" :key="i">{{ item }}</el-checkbox>
              </el-checkbox-group>
            </el-form-item>
          </el-tab-pane>
          <el-tab-pane label="商品属性" name='2'>
            <el-form-item :label="item.attr_name" v-for="item in onlyTableData" :key="item.attr_id">
              <el-input v-model="item.attr_vals"></el-input>
            </el-form-item>
          </el-tab-pane>
          <el-tab-pane label="商品图片" name='3'>
            <!--
             action 表示图片要上传到的后台API地址
             handlePreview 点击预览触发
             handleRemove 删除图片触发
             fileList 指定文件列表
             list-type 指定预览组件的呈现方式
            -->
            <el-upload class="upload-demo" :action="uploadURL" :on-preview="handlePreview" :on-remove="handleRemove" list-type="picture" :headers="headersObj" :on-success="handleSuccess">
              <el-button size="small" type="primary">点击上传</el-button>
            </el-upload>
          </el-tab-pane>
          <el-tab-pane label="商品内容" name='4'>
            <!-- 富文本编辑器组件 Two-way Data-Binding -->
            <quill-editor ref="myQuillEditor" v-model="addForm.goods_introduce" />
            <!--添加按钮-->
            <el-button type="primary" class="AddBtn" @click="addShop">添加商品</el-button>
          </el-tab-pane>
        </el-tabs>
      </el-form>
    </el-card>
    <!--    图片预览-->
    <el-dialog title="图片预览" :visible.sync="previewDialogVisible" width="50%">
      <img :src="previewPath" alt="" class="perviewImg" />
    </el-dialog>
  </div>
</template>

<script>
  import _ from 'lodash'
  import {
    request
  } from '../../../../../../network/request'
  export default {
    name: 'Add',
    data() {
      return {
        activeIndex: '0',
        tabPosition: 'left',
        addFormRules: {
          goods_name: [{
              required: true,
              message: '请输入商品的名称',
              trigger: 'blur'
            },
            {
              min: 2,
              max: 30,
              message: '请输入 2 ~ 30 范围的字符',
              trigger: 'blur'
            }
          ],
          goods_price: [{
            required: true,
            message: '请输入商品的价格',
            trigger: 'blur'
          }],
          goods_weight: [{
            required: true,
            message: '请输入商品的重量',
            trigger: 'blur'
          }],
          goods_number: [{
            required: true,
            message: '请输入商品的数量',
            trigger: 'blur'
          }],
          goods_cat: [{
            required: true,
            message: '请选择商品分类',
            trigger: 'blur'
          }]
        },
        addForm: {
          goods_name: '',
          goods_price: 0,
          goods_weight: 0,
          goods_number: 0,
          goods_cat: [],
          // 图片的数组
          pics: [],
          // 商品的详情描述
          goods_introduce: '',
          attrs: []
        },
        cateList: [],
        cateProps: {
          value: 'cat_id',
          label: 'cat_name',
          children: 'children'
        },
        manyTableData:[],
        onlyTableData:[],
        uploadURL:'http://127.0.0.1:8888/api/private/v1/upload',
        // 图片上传组件的headers请求头对象
        headersObj: {
          Authorization: window.sessionStorage.getItem('token')
        },
        // 预览图片的路径
        previewPath: '',
        // 图片预览的对话框
        previewDialogVisible: false,
      }
    },
    methods: {
      getCateList() {
        request({
          url: '/categories'
        }).then(res => {
          this.cateList = res.data
          // console.log(this.cateList)
        })
      },
      handleChange() {
        if(this.addForm.goods_cat.length!==3){
          this.addForm.goods_cat=[]
        }
        // console.log(this.addForm.goods_cat)
      },
      //离开当前tab页
      beforeTabLeave(activeName,oldActiveName){
      //  console.log(oldActiveName)
      //  console.log(activeName)
        if(oldActiveName==='0'&&this.addForm.goods_cat.length!==3||this.addForm.goods_name===''){
          this.$message.error('请先选择商品分类!或填写商品名称')
          return false
        }
      },
      tabClicked(){
        //证明访问的是动态列表
        if(this.activeIndex==='1'){
          // console.log('动态参数')
          request({
            url:`/categories/${this.cateId}/attributes`,
            params:{
              sel:'many'
            }
          }).then(res=>{
            res.data.forEach(item=>item.attr_vals=item.attr_vals?item.attr_vals.split(' '):[])
            this.manyTableData=res.data
            // console.log(this.manyTableData)
          })
        }
        if(this.activeIndex==='2'){
          request({
            url:`/categories/${
              this.cateId
            }/attributes`,
            params:{
              sel:'only'
            }
          }).then(res=>{
            this.onlyTableData=res.data
          })
        }
      },
      // 点击图片预览时触发
      handlePreview(file) {
        this.previewPath = file.response.data.url
        this.previewDialogVisible = true
        // console.log('预览图片', file)
      },
      // 处理移除图片的操作
      handleRemove(file) {
        // console.log(file)
        // 1. 获取将要删除的图片的临时路径
        const filePath = file.response.data.tmp_path
        // 2. 从 pics 数组中找到这个图片的对应的索引值
        const index = this.addForm.pics.findIndex(x => x.pic === filePath)
        // 3. 调用数组的splice方法,把图片信息对象,从pics数组中移除
        this.addForm.pics.splice(index, 1)
        // console.log('移除图片', file, this.addForm)
      },
      // 监听图片上传成功的事件
      handleSuccess(resposne) {
        // 1. 拼接得到一个图片信息对象
        const picInfo = { pic: resposne.data.tmp_path }
        // 2. 将图片信息对象,push到pics数组中
        this.addForm.pics.push(picInfo)
        // console.log(resposne)
        // console.log(this.addForm)
      },
      addShop(){
        this.$refs.addFormRef.validate(async valid => {
          if (!valid) {
            return this.$message.error('请填写必要的表单项')
          }
          // 执行添加的业务逻辑
          //深拷贝
          const form = _.cloneDeep(this.addForm)
          form.goods_cat = form.goods_cat.join(',')
          // 处理动态参数
          this.manyTableData.forEach(item => {
            const newInfo = { attr_id: item.attr_id, attr_vals: item.attr_vals }
            this.addForm.attrs.push(newInfo)
          })
          // 处理静态参数
          this.onlyTableData.forEach(item => {
            const newInfo = { attr_id: item.attr_id, attr_vals: item.attr_vals }
            this.addForm.attrs.push(newInfo)
          })
          form.attrs = this.addForm.attrs
          // console.log(form)
          // 发起请求添加商品数据
          // 商品的名称必须是唯一的
          request({
            url:`/goods`,
            method:'post',
            data:form
          }).then(res=>{
            this.$router.push('/goods')
            return this.message.success('添加商品成功!')
          })
        })
      }
    },
    created() {
      this.getCateList()
    },
    computed:{
      cateId(){
        if(this.addForm.goods_cat.length===3){
          return this.addForm.goods_cat[2]
        }
        return null
      }
    }
  }
</script>

